def add(a, b):
    """Return the sum of two numbers."""
    return a + b


def is_even(n):
    """Return True if n is even."""
    return n % 2 == 0


def reverse_string(s):
    """Return the reversed version of a string."""
    return s[::-1]


def greet(name):
    """Return a greeting string."""
    return f"Hello, {name}!"
